

# 普通参数，默认参数，收集参数，关键字参数，命名关键字参数


# 普通参数就是位置参数，也叫顺序参数，也是必须传递的参数
def func(x,y):
    print(x,y)


#比如要求有三个参数，x,y,i至少要有传递2个，那么这时最后一个参数就是默认参数，但是默认参数，需要在定义形参时，指定默认值
def func(x,i=None):
    print("---------------")
    print(x,i)
    print("===============")
    return x+i

if __name__ == '__main__':
    a = func(1,func(2,func(3,200)))
